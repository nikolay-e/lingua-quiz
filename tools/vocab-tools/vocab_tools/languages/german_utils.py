def is_likely_german_noun(word: str) -> bool:
    word_lower = word.lower()
    if word_lower in GERMAN_LIKELY_NOUNS:
        return True
    for suffix in GERMAN_NOUN_SUFFIXES:
        if word_lower.endswith(suffix) and len(word_lower) > len(suffix) + 1:
            return True
    return False


def looks_like_german_noun_by_suffix(word: str) -> bool:
    word_lower = word.lower()
    for suffix in GERMAN_NOUN_SUFFIXES:
        if word_lower.endswith(suffix):
            return True
    return False


def capitalize_german_noun(word: str, pos_tag: str | None = None) -> str:
    if pos_tag in {"NOUN", "PROPN"}:
        return word.capitalize()
    if is_likely_german_noun(word):
        return word.capitalize()
    return word


